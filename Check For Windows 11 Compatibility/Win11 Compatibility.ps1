$memory=Get-WMIObject -class win32_ComputerSystem | Select-Object -Expand TotalPhysicalMemory
$MemoryGB=[Math]::ceiling($memory/1024/1024/1024)
if ($MemoryGB -ge 4){
	$RAM = "Pass"
} else {
	$RAM = "Fail"
}

