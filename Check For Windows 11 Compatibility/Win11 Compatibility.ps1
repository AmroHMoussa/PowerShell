	$ProcCompat = "Pass"
	} else {
	$ProcCompat = "Fail"
}
 
#Check if a TPM chip is present
if ([bool](Get-Tpm).tpmpresent -eq "True") {
	$TPMPF = "Pass"
	} else {
	$TPMPF = "Fail"
}

#Check for TPM 2.0 
if ((Get-Tpm).ManufacturerVersionFull20 -contains "not supported") {
	$Tpm20 = "Fail"
} else {
	$Tpm20 = "Pass"
}
if ((Get-Tpm).ManufacturerVersionFull20 -eq $Null) {
	$Tpm20 = "Fail"}

