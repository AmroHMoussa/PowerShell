try{
    $Secureboot=Confirm-SecureBootUEFI -ErrorAction Stop
    $UEFI = $True
}
Catch {
    $UEFI = "Fail"
}
if ($Secureboot = "True") {
	$UEFI = "Pass"
} else {
	$UEFI = "Fail"
}

